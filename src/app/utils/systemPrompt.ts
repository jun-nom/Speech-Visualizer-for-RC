/**
 * OpenAIへ送るシステムプロンプトをここで管理します。
 * Supabaseへのデプロイ不要で、このファイルを編集するだけで即反映されます。
 */
export function buildSystemPrompt(textDensity: string): string {
  const densityConfig = DENSITY_CONFIG[textDensity] ?? DENSITY_CONFIG.high;
  return buildPrompt(densityConfig);
}

